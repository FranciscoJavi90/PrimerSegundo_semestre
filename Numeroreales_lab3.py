#El programa, dados como datos dos números reales, calculará la suma, la resta y multiplicación de dichos números. 
a = int(input('Introduzca primer número real para las operaciones: '))
b = int(input('Introduzca segundo número real para las operaciones: '))
resultadoS = a + b
print('Resultado suma: ',resultadoS)
resultadoR = a - b
print('Resultado resta: ',resultadoR)
resultadoM = a * b
print('Resultado multiplicación: ',resultadoM)
